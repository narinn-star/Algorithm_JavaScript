const readline = require('readline');
const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout,
});

let input = [];

const solution = (N, map) => {
  let count = 0;

  const dfs = (x, y, dir) => {
    if (x === N - 1 && y === N - 1) {
      count++;
      return;
    }

    // ê°€ë¡œ
    if (dir === 0 || dir === 2) {
      if (y + 1 < N && map[x][y + 1] === 0) {
        dfs(x, y + 1, 0);
      }
    }

    // ì„¸ë¡œ
    if (dir === 1 || dir === 2) {
      if (x + 1 < N && map[x + 1][y] === 0) {
        dfs(x + 1, y, 1);
      }
    }

    // ëŒ€ê°
    if (
      x + 1 < N &&
      y + 1 < N &&
      map[x + 1][y] === 0 &&
      map[x][y + 1] === 0 &&
      map[x + 1][y + 1] === 0
    ) {
      dfs(x + 1, y + 1, 2);
    }
  };

  dfs(0, 1, 0);

  console.log(count);
};

rl.on('line', function (line) {
  input.push(line.split(' ').map(Number));
}).on('close', function () {
  const N = Number(input.shift());
  const map = input;
  solution(N, map);
  process.exit();
});
